							<div class="accordion-item-header">Qu’est ce que le DPC ?</div>
							<div class="accordion-item-body">
								<div class="accordion-item-body-content">
									👉 Le Développement Professionnel Continu (DPC) a pour but de maintenir et de mettre à jour les connaissances et les compétences, ainsi que d'améliorer les pratiques dans le domaine de la santé. <br><br>

									💡C'est une exigence pour tous les professionnels de la santé, indépendamment de leur mode ou secteur d'activité. <br><br>
									
									🔑 Le DPC prend en charge la formation de tous les professionnels de santé travaillant en libéral, ainsi que de ceux employés dans des centres de santé conventionnés.
									
