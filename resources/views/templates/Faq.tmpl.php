								Qu'est ce que l'obligation triennale du DPC ?
							</div>
							<div class="accordion-item-body rounded-5">
								<div class="accordion-item-body-content">
									👉 Tous les professionnels de santé sont tenus de suivre le Dispositif de Formation Continue obligatoire appelé Développement Professionnel Continu (DPC), quel que soit leur mode d'exercice. <br><br>
									💡Au cours d'une période de trois ans (2023-2025), chaque praticien doit accomplir au moins deux types d'actions différentes, à savoir : <br><br>
									<ul style="padding-left: 1rem">
										<li>Formation continue</li>
										<li>Évaluation des pratiques professionnelles (EPP)</li>
										<li>Gestion des risques. </li>
									</ul>
									🔑 Pour répondre à cette obligation triennale de DPC, la méthode la plus simple est de suivre l'un des programmes intégrés proposés qui combine deux types d'actions en une seule formation : formation continue et EPP.
